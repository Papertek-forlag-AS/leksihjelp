/**
 * build-popup-views-css.js — Generates extension/styles/popup-views.css from
 * extension/styles/popup.css by scoping every selector under the host element
 * #leksihjelp-sidepanel-root. Drops body/html/.app rules so the file is safe
 * to inject into a host page (used by the lockdown sidepanel host; see
 * Phase 30 Plan 01 sub-step E).
 *
 * Run: node scripts/build-popup-views-css.js  (or npm run build-popup-views-css)
 */

const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');
const SRC = path.join(repoRoot, 'extension', 'styles', 'popup.css');
const DST = path.join(repoRoot, 'extension', 'styles', 'popup-views.css');
const PREFIX = '#leksihjelp-sidepanel-root';
const DROP_SELECTORS_EXACT = new Set(['body', 'html', '.app']);

let src = fs.readFileSync(SRC, 'utf8');

const comments = [];
src = src.replace(/\/\*[\s\S]*?\*\//g, (m) => {
  const i = comments.push(m) - 1;
  return ` C${i} `;
});

function transformSelector(sel) {
  sel = sel.trim();
  if (!sel) return null;
  if (sel === ':root') return PREFIX;
  if (sel === '*') return `${PREFIX} *`;
  if (sel === ':focus-visible') return `${PREFIX} :focus-visible`;
  if (sel === '[data-theme="dark"]') return `${PREFIX}[data-theme="dark"]`;
  const dm = sel.match(/^\[data-theme="dark"\]\s+(.+)$/s);
  if (dm) {
    const rest = dm[1].trim();
    if (DROP_SELECTORS_EXACT.has(rest)) return null;
    return `${PREFIX}[data-theme="dark"] ${rest}`;
  }
  if (DROP_SELECTORS_EXACT.has(sel)) return null;
  return `${PREFIX} ${sel}`;
}

function transformSelectorList(text) {
  const parts = text.split(',').map(s => s.trim()).filter(Boolean);
  const out = parts.map(transformSelector).filter(s => s !== null);
  if (out.length === 0) return null;
  return out.join(', ');
}

let out = '';
let buf = '';
let depth = 0;
let inAtBlockDepth = -1;
let inAtKeyframesDepth = -1;
let dropDepth = -1;
let inStr = null;

for (let p = 0; p < src.length; p++) {
  const c = src[p];

  if (inStr) {
    buf += c;
    if (c === inStr && src[p - 1] !== '\\') inStr = null;
    continue;
  }
  if (c === '"' || c === "'") {
    inStr = c;
    buf += c;
    continue;
  }

  if (c === '{') {
    depth++;
    const text = buf;
    const tokens = text.split(/( C\d+ )/);
    let lastSelIdx = -1;
    for (let i = tokens.length - 1; i >= 0; i--) {
      if (!/^ C\d+ $/.test(tokens[i]) && tokens[i].trim() !== '') {
        lastSelIdx = i;
        break;
      }
    }
    if (lastSelIdx === -1) {
      out += text + c;
      buf = '';
      continue;
    }
    const before = tokens.slice(0, lastSelIdx).join('');
    const selectorRaw = tokens[lastSelIdx];
    const after = tokens.slice(lastSelIdx + 1).join('');
    const selTrim = selectorRaw.trim();

    if (/^@(-webkit-)?keyframes\b/.test(selTrim)) {
      inAtKeyframesDepth = depth;
      out += before + selectorRaw + after + c;
      buf = '';
      continue;
    }
    if (inAtKeyframesDepth >= 0 && depth > inAtKeyframesDepth) {
      out += before + selectorRaw + after + c;
      buf = '';
      continue;
    }
    if (selTrim.startsWith('@')) {
      inAtBlockDepth = depth;
      out += before + selectorRaw + after + c;
      buf = '';
      continue;
    }

    const transformed = transformSelectorList(selTrim);
    if (transformed === null) {
      dropDepth = depth;
      out += before;
      buf = '';
      continue;
    }
    out += before + transformed + ' ' + c;
    buf = '';
    continue;
  }

  if (c === '}') {
    if (dropDepth > 0 && depth >= dropDepth) {
      depth--;
      if (depth < dropDepth) dropDepth = -1;
      buf = '';
      continue;
    }
    if (inAtKeyframesDepth >= 0 && depth === inAtKeyframesDepth) inAtKeyframesDepth = -1;
    if (inAtBlockDepth >= 0 && depth === inAtBlockDepth) inAtBlockDepth = -1;
    depth--;
    out += buf + c;
    buf = '';
    continue;
  }

  buf += c;
}
out += buf;

out = out.replace(/ C(\d+) /g, (_, i) => comments[+i]);

const header = `/* popup-views.css — auto-generated from popup.css for downstream embedding (lockdown sidepanel host).
   All rules scoped under ${PREFIX} so the file is safe to inject into a host page.
   Drops body/html/.app rules. Keeps :root variables under the host scope.
   Source: extension/styles/popup.css — DO NOT EDIT THIS FILE BY HAND. */

`;

fs.writeFileSync(DST, header + out);
console.log(`wrote ${DST}, ${out.split('\n').length} lines`);
